package APP;

public abstract class Figura {
	
	/**
	 * Devuelve el area de la figura en metros cuadraddos
	 * 
	 * @return		el area de la figura
	 */
	public abstract double area();
	
	/**
	 * Devueleve el perimetro de la figura en metros
	 * 
	 * @return		el perimetro de la figura en metros
	 */
	public abstract double perimetro();
}
